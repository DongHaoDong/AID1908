"""
    经理：曹操,刘备,孙权
    技术：曹操,刘备,张飞,关羽
"""
set01 = {"曹操", "刘备", "孙权"}
set02 = {"曹操", "刘备", "张飞", "关羽"}
# 是经理也是技术的都有谁
print(set01 & set02)
# 是经理不是技术的都有谁
print(set01 - set02)
# 是技术不是经理的都有谁
print(set02 - set01)
# 张飞是经理吗？
print("张飞" in set01)
# 身兼一职的都有谁
print(set01 ^ set02)
# 经理和技术总共有多少人？
print(len(set01 | set02))

