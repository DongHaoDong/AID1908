"""
    核心数据类型
"""
# 1.None
a01 = "苏大强"
# 解除变量与数据的绑定关系
a01 = None

# 使用None占位
sex = None

# 2.整型int
# 十进制
num01 = 20

# 二进制：10
print(0b10)

# 八进制：0
print(0o10)

# 十六进制:0 -- 9,A -- F
print(0x10)

# 3.浮点数float
print(1.5)
# 科学计数法:表示过小或过大的值很明确
# 1.23e-33
print(0.00000000000000000000000000000000123)

# 4. 字符串str
print("1.5")
a = 10
print(a)    # 打印变量 10
print("a")  # 打印字符串 a

